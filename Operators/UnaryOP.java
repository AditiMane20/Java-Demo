//Unary Operators(-,+,++,--)
class UnaryOP{
	public static void main(String[] args){
		int x=10;
		int y=7;

		System.out.println(++x); //11
		System.out.println(++y); //8

		System.out.println(--x); //10
		System.out.println(--y); //7

		System.out.println(x); //10
		System.out.println(y); //7
	}
}
